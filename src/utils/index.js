/**
 * 防抖动
 * @param func
 * @param wait
 * @param immediate
 * @return {function(...[*]=): *}
 */
export function debounce(func, wait, immediate) {
  let timeout, args, context, timestamp, result

  const later = function() {
    // 据上一次触发时间间隔
    const last = +new Date() - timestamp

    // 上次被包装函数被调用时间间隔last小于设定时间间隔wait
    if (last < wait && last > 0) {
      timeout = setTimeout(later, wait - last)
    } else {
      timeout = null
      // 如果设定为immediate===true，因为开始边界已经调用过了此处无需调用
      if (!immediate) {
        result = func.apply(context, args)
        if (!timeout) context = args = null
      }
    }
  }

  return function(...args) {
    context = this
    timestamp = +new Date()
    const callNow = immediate && !timeout
    // 如果延时不存在，重新设定延时
    if (!timeout) timeout = setTimeout(later, wait)
    if (callNow) {
      result = func.apply(context, args)
      context = args = null
    }

    return result
  }
}

/**
 * 判断类型
 * @author Daniel
 * @copyright 2019/03/21
 * @param {type} anything
 * @returns ['undefined','null','object','array','function','number','boolean','string']等 String
 * @description 目前可以支持17种类型
 */
export function getType(item) {
  // ie6中null和undefined会被Object.prototype.toString成[object Object]
  // item == null只限null和undefined
  if (item == null) return item + ''
  const TYPES_OBJ = {}
  const TYPES_VALUES = 'Undefined Null String Number Boolean Object Symbol Function Array Arguments Date Math JSON RegExp Error Location History'
  TYPES_VALUES.split(' ').map(val => {
    TYPES_OBJ['[object ' + val + ']'] = val.toLowerCase()
  })
  return TYPES_VALUES[Object.prototype.toString.call(item)]
}

